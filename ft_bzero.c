#include "libft.h"

void	bzero(void *s, size_t n)
{
	size_t i;

	char	*s_prim;

	s_prim = (char *)s;
	i = 0;
	while (i < n)
	{
		*(s_prim + i)= 0;
		i++;
	}
}
